#include <bits/stdc++.h> 
using namespace std;

    /*
        round, floor, ceil
    */
int main()
{
    // 1. round - жуықтау     round(1.4) = 1    round(7.2) = 7   round(14.5236) = 15
    cout << round(1.4) << endl;
    cout << round(7.2) << endl;
    cout << round(14.5236) << endl;

    // 2. floor - пол, жер   floor(1.9) = 1   floor(2.4) = 2     
    cout << floor(1.9) << endl; 
    cout << floor(2.4) << endl; 


    // 3.  Ceil - Потолок, төбе     ceil(2.4) = 3    ceil(2.1) = 3    ceil(2.0) = 2
    cout << ceil(2.4) << endl;
    cout << ceil(2.1) << endl;
    cout << ceil(2.0) << endl;

    
    return 0;
}